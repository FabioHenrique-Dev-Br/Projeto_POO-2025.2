    private void Buttun_CadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Buttun_CadastroActionPerformed
     
        String nome = Nome_User.getText();
        String email = Email_User.getText();

        // ✅ Validação primeiro
        if (nome.isBlank() || email.isBlank()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Preencha nome e email!");
            return;
        }

        try {
            simplelibrary.poo.model.Usuario usuario =
                    new simplelibrary.poo.model.Usuario(nome, email, "ATIVO");

            simplelibrary.poo.dao.UsuarioDAO dao =
                    new simplelibrary.poo.dao.UsuarioDAO();

            dao.cadastrar(usuario);

            javax.swing.JOptionPane.showMessageDialog(this, "Usuário cadastrado com sucesso!");

            Nome_User.setText("");
            Email_User.setText("");

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao cadastrar usuário!");
            e.printStackTrace();
        }

    }//GEN-LAST:event_Buttun_CadastroActionPerformed

