    private void Button_Cadastro_BooksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_Cadastro_BooksActionPerformed
        try {

            String titulo = Titulo_txt.getText();
            String genero = Gên_txt.getText();
            int ano = Integer.parseInt(Year_txt.getText());
            int idAutor = Integer.parseInt(ID_Autor_txt.getText());

            if (titulo.isBlank() || genero.isBlank()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Preencha todos os campos!");
                return;
            }

            simplelibrary.poo.model.Livro livro =
                    new simplelibrary.poo.model.Livro(titulo, ano, genero, idAutor, "DISPONIVEL");

            simplelibrary.poo.dao.LivroDAO dao =
                    new simplelibrary.poo.dao.LivroDAO();

            dao.cadastrar(livro);

            javax.swing.JOptionPane.showMessageDialog(this, "Livro cadastrado com sucesso!");

            Titulo_txt.setText("");
            Gên_txt.setText("");
            Year_txt.setText("");
            ID_Autor_txt.setText("");

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao cadastrar livro!");
            e.printStackTrace();
        }
    }//GEN-LAST:event_Button_Cadastro_BooksActionPerformed

