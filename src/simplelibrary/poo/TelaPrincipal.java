    private void Listar_Users_cadastradosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Listar_Users_cadastradosActionPerformed
        try {
            UsuarioDAO dao = new UsuarioDAO();
            List<Usuario> usuarios = dao.listar();

            DefaultTableModel model = (DefaultTableModel) Lista_Users.getModel();
            model.setRowCount(0);

            for (Usuario u : usuarios) {
                model.addRow(new Object[]{
                    u.getId(),        // ✅ CORRIGIDO AQUI
                    u.getNome(),
                    u.getEmail(),
                    u.getStatus()
                });
            }

        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao listar usuários!");
        }
    }//GEN-LAST:event_Listar_Users_cadastradosActionPerformed

